/**
 * This class impliments evaluate to return the result
 * the cosine of the parameter
 *
 * @author      Kyle Riedemann
 * @version     1.0
 * @since       02-10-2015
 */
public class CosFunc extends Function {
    /**
     * @param x The value of the variable to be evaluated
     * @return Math.cos(x) The result of the cosine of x
     */
	public double evaluate(double x){
		return Math.cos(x);
	}
}